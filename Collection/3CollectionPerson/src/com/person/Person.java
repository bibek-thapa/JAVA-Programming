package com.person;

public class Person {
	int id;
	String name;
	float salary;
	public Person() {}
	public Person(int id, String name, float salary) {
	
		this.id = id;
		this.name = name;
		this.salary = salary;
	}
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public float getSalary() {
		return salary;
	}
	
	
	
	

}
